class Animal:
    coat = 'Texture and color of animal'
    life_span = 0
    diet = 'What they eat'
    activity_level = 'Energy level'

class Cat(Animal):
    wisker_count: 0
    max_jump_height: 0

class Dog(Animal):
    breed: 'Type of dog'
    job: 'What the dog does'
    

    
    
    
